package week03;

public class AssignmentOperators {

    public static void main(String[] args) {
        
        int i = 100;

        System.out.println("i = " + i);
        
        i += 10;

        System.out.println("i = " + i);

        i -= 5;

        System.out.println("i = " + i);

        i *= 3;

        System.out.println("i = " + i);

        i /= 4;

        System.out.println("i = " + i);

        i %= 5;

        System.out.println("i = " + i);
    }
}
