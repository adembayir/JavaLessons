package day03_EscapeSequences;
/*
escape sequences: MUST be g'ven with double quote
    \n: starts a new line
    \t: does the same work with tab button.
    \\: prints backward slash
    \": prints double quotes
 */

public class EscapeSequences {

    public static void main(String[] args) {

        System.out.println("Java \nPyton \nC#");

        System.out.println("-----------------------------------------------");

        System.out.println("Hello Cydeo \nHow are you all today? \nIt's nice to see you all! \nWhat class do we have next week?");

        System.out.println("-----------------------------------------------");

        System.out.println("\tJava is cool Programming Language");

        System.out.println("-----------------------------------------------");

        System.out.println("/ \\"); // in order to print one backslash we need to give two backward slashes

        System.out.println("-----------------------------------------------");

        System.out.println("My favorite TV show is \"Game of Thrones\"");

    }


}
