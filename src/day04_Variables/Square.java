package day04_Variables;

public class Square {

    public static void main(String[] args) {

        double side = 8.5;

        double area = side * side;
        double perimeter = 4 * side;

        System.out.println("If one side of an square = " + side);
        System.out.println("Then the area of square = " + area);
        System.out.println("And the perimeter of square = " + perimeter);
    }
}
