package day15_ForLoop;

public class ForLoopIntro {

    public static void main(String[] args) {

        System.out.println("Hello World!"); // need to print 10 times more.

        for (int i = 1 ; i <= 10 ; i++ ){ //i used 'for loop' to print statement 10 times more.
            System.out.println("Hello World!"); // initialization ; condition ; iteration
        }


    }
}
